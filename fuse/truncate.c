#include <julea-config.h>

#include "juleafs.h"

#include <errno.h>

int
jfs_truncate (char const* path, off_t size)
{
	JURI* uri;
	int ret = -ENOENT;

	(void)size;

	if ((uri = jfs_get_uri(path)) == NULL)
	{
		goto end;
	}

	if (!j_uri_get(uri, NULL))
	{
		goto end;
	}

	if (j_uri_get_item(uri) != NULL)
	{
		/* FIXME */
		//j_item_set_size(j_uri_get_item(uri), size);

		ret = 0;
	}

end:
	if (uri != NULL)
	{
		j_uri_free(uri);
	}

	return ret;
}
